async def resolve_ts_code(session, name: str, prefer_cb: bool = False):
    name_clean = (name or "").replace(" ", "").replace("XD", "")
    if not name_clean:
        return None
    if prefer_cb:
        r = await session.execute(text(
            "SELECT ts_code FROM cb_basic WHERE bond_short_name=:n OR bond_short_name LIKE :nl LIMIT 1"
        ), {"n": name_clean, "nl": f"%{name_clean}%"})
        row = r.fetchone()
        if row:
            return row[0]
    r = await session.execute(text(
        "SELECT ts_code FROM stock_basic WHERE REPLACE(name,' ','')=:n AND list_status='L' LIMIT 1"
    ), {"n": name_clean})
    row = r.fetchone()
    if row:
        return row[0]
    r = await session.execute(text(
        "SELECT ts_code FROM stock_basic WHERE REPLACE(name,' ','') LIKE :nl AND list_status='L' LIMIT 1"
    ), {"nl": f"%{name_clean}%"})
    row = r.fetchone()
    return row[0] if row else None


async def main():
    async with async_session() as s:
        await s.execute(text(
            "DELETE FROM daily_sector_review WHERE trade_date=:d AND source='jiuyan'"
        ), {"d": TRADE_DATE})

        size_map = {sec: (size, rank) for sec, size, rank in JIUYAN_SECTORS}
        n = 0
        unresolved = []

        for board, dtb, sector, ts_code_known, name, ftime, mv_yi, amt_yi, kw in JIUYAN_DATA:
            ts_code = ts_code_known
            if not ts_code:
                ts_code = await resolve_ts_code(s, name)
            if not ts_code:
                unresolved.append((sector, name))
            size, rank = size_map.get(sector, (None, None))
            await s.execute(text(
                "INSERT INTO daily_sector_review "
                "(trade_date, source, sector_name, sector_rank, sector_size, "
                " ts_code, stock_name, board_count, days_to_board, limit_time, "
                " float_mv, amount, keywords, is_main_line, raw_meta) VALUES "
                "(:td,'jiuyan',:sec,:rank,:size,:tc,:nm,:bc,:dtb,:lt,:fmv,:amt,:kw,true,:rm)"
            ), {
                "td": TRADE_DATE, "sec": sector, "rank": rank, "size": size,
                "tc": ts_code, "nm": name, "bc": board, "dtb": dtb, "lt": ftime,
                "fmv": mv_yi * 1e8 if mv_yi else None,
                "amt": amt_yi * 1e8 if amt_yi else None,
                "kw": kw,
                "rm": json.dumps({"scope": "daily"}, ensure_ascii=False),
            })
            n += 1

        await s.commit()

    print(f"jiuyan {TRADE_DATE} 入库: {n} 行")
    if unresolved:
        print(f"\n未匹配 ts_code {len(unresolved)} 条:")
        for sec, nm in unresolved:
            print(f"  [{sec}] {nm}")


if __name__ == "__main__":
    asyncio.run(main())
