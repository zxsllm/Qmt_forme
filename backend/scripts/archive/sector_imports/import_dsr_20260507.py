def parse_board(tag: str) -> tuple[int, int]:
    """X天Y板 → (days_to_board, board_count)；'1' → (1,1) 首板"""
    if not tag or tag == "1":
        return 1, 1
    if "天" in tag and "板" in tag:
        d = tag.split("天")[0]
        b = tag.split("天")[1].replace("板", "")
        return int(d), int(b)
    return 1, 1


