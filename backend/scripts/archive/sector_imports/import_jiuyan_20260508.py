async def main():
    async with async_session() as s:
        # 1. 清理 5/8 jiuyan 旧数据
        r = await s.execute(text(
            "DELETE FROM daily_sector_review WHERE trade_date=:d AND source='jiuyan'"
        ), {"d": TD})
        print(f"清理 5/8 jiuyan 旧数据: {r.rowcount} 行")
        await s.commit()

        # 2. 韭研入库
        n = 0
        for sec, sec_size, board_tag, ts_code, name, lt, fmv, amount, kw in JIUYAN:
            d, b = parse_board(board_tag)
            await s.execute(text("""
                INSERT INTO daily_sector_review (
                    trade_date, source, sector_name, sector_size,
                    ts_code, stock_name, board_count, days_to_board, limit_time,
                    float_mv, amount, keywords, is_main_line, raw_meta, created_at
                ) VALUES (
                    :d, 'jiuyan', :sec, :ss, :tc, :nm, :b, :dd, :lt,
                    :fmv, :am, :kw, false, CAST(:rm AS jsonb), NOW()
                )
            """), {
                "d": TD, "sec": sec, "ss": sec_size,
                "tc": ts_code, "nm": name, "b": b, "dd": d, "lt": lt,
                "fmv": fmv, "am": amount, "kw": kw,
                "rm": json.dumps({"scope": "daily"}),
            })
            n += 1
        await s.commit()
        print(f"韭研入库: {n} 行")

        # 3. 校验：检查 ts_code → name 是否与 stock_basic 一致
        print("\n=== ts_code/name 校验 ===")
        r = await s.execute(text("""
            SELECT dsr.ts_code, dsr.stock_name, sb.name
            FROM daily_sector_review dsr
            LEFT JOIN stock_basic sb ON sb.ts_code = dsr.ts_code
            WHERE dsr.trade_date=:d AND dsr.source='jiuyan'
              AND (sb.name IS NULL OR REPLACE(sb.name,' ','') <> dsr.stock_name)
            ORDER BY dsr.ts_code
        """), {"d": TD})
        diffs = r.fetchall()
        if diffs:
            print(f"发现 {len(diffs)} 处 ts_code 与 stock_basic.name 不一致：")
            for ts, dsr_name, sb_name in diffs:
                print(f"  {ts}  韭研={dsr_name}  stock_basic={sb_name}")
        else:
            print("全部 ts_code/name 与 stock_basic 一致")

        # 4. 板块汇总
        print("\n=== 板块汇总（按 sector_size 降序） ===")
        r = await s.execute(text("""
            SELECT sector_name, MAX(sector_size), COUNT(*)
            FROM daily_sector_review
            WHERE trade_date=:d AND source='jiuyan'
            GROUP BY sector_name
            ORDER BY MAX(sector_size) DESC NULLS LAST
        """), {"d": TD})
        for sec, ss, cnt in r.fetchall():
            print(f"  {sec:<12} sector_size={ss}  入库={cnt}")


if __name__ == "__main__":
    asyncio.run(main())
