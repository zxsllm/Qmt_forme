# ---------------------------------------------------------------------------
# 给 LLM v2 prompt 用的 taxonomy 渲染
# ---------------------------------------------------------------------------

def render_taxonomy_for_prompt() -> str:
    """渲染成 prompt 可读的字符串 — 按大筐分组列出已知细分主线。

    用途：让 LLM 知道"参考粒度"（不要拆得过细，也不要合并到大筐），
    每个大筐下展示已知的细分主线。新词允许 LLM 自己取名（保留原名）。
    """
    # 按大筐分组聚合已知细分
    parent_groups: dict[str, list[str]] = {}
    for sub, parent in SUBSECTOR_TO_PARENT.items():
        parent_groups.setdefault(parent, []).append(sub)

    # 按用户重要性排序大筐
    PARENT_ORDER = [
        "算力", "国产芯片", "机器人", "商业航天",
        "电池产业链", "电力设备", "氢能", "光伏",
        "稀有金属", "医药", "体育", "汽车产业链",
    ]
    lines = ["（参考粒度：当日资金抱团的细分主线，不要合并到大筐，也不要拆得比下面更细）"]
    for parent in PARENT_ORDER:
        subs = parent_groups.get(parent, [])
        if not subs:
            continue
        # 截断展示（每大筐最多 12 个细分，避免 prompt 过长）
        shown = subs[:12]
        lines.append(f"  [{parent}] 下的细分主线：{' / '.join(shown)}")
    return "\n".join(lines)


